          NUESTRO PLAN

          
        </p>
        <p style="text-align: justify;font-size: medium;" class="lead">
          <ul style="text-align: justify;font-size: medium;" class="lead">
            <li>  Menos Contacto, Más conexión. El hotel ofrecerá soluciones tecnológicas para minimizar el contacto al check-in, durante la estadía y el check-out.</li>
            <li> Suministro de Elementos de protección personal. Todos los empleados del hotel usarán mascarilla y elementos de protección personal necesarios como parte de sus uniformes. Los huéspedes deberán usar mascarilla personal y seguir las regulaciones locales.</li>
            <li> Distanciamiento físico. Los huéspedes y empleados respetarán la distancia física al menos a 2 metros de otras personas con las que no viajen.</li>
            <li> Mamparas de protección. Contaremos con pantallas transparentes u otras barreras físicas en recepción.</li>
            <li> Énfasis en la higiene y limpieza. El hotel contará con un plan de higiene y los empleados estarán informados para seguir los protocolos, higiene personal, distanciamiento social y uso de elementos de protección, en cumplimiento con las normas y priorizando el cuidado de nuestros huéspedes. Las estaciones de desinfección de manos para los huéspedes se ubicarán en lugares con alto tráfico y espacios públicos.</li>
            <li> Los protocolos de limpieza actualizados requerirán una desinfección frecuente de los artículos de alto contacto como la desinfección y ventilación de las habitaciones después de su uso. Siempre utilizando los productos y procedimientos correctos para eliminar el Covid-19.</li>
            <li> Capacitación de limpieza. Los empleados tomarán todas las capacitaciones adicionales y recomendadas por los organismos competentes sobre Covid-19.</li>
            <li> PROTOCOLOS DE SALUD. Toma de temperatura de manera discreta y no invasiva a empleados y proveedores. También se solicitará a los huéspedes la revisión para seguridad de todos. Aquellos con temperatura mayor o igual a 37,5º les será negado el ingreso y serán dirigidos al servicio de salud local.</li>
            <ul>
              <li>  Ante Síntomas Covid-19 positivos. Ante un empleado o huésped con síntomas positivos, se dará aviso a la autoridad sanitaria local, explicando la situación y la información relevante, como sus síntomas actuales. Se procederá de acuerdo a lo indicado por la autoridad sanitaria local. </li>
              <li> Prevención e información. El Hotel responderá con rapidez las preocupaciones de empleados y huéspedes y seguirán todas las directrices actuales vigentes de salud pública. Los empleados que se sientan enfermos serán alentados a quedarse en casa. Aquellos que muestren síntomas se les pedirá control médico, que se auto aíslen y cumplan la cuarentena antes de regresar a trabajar. Todas las acciones relativas al Covid-19 serán constantemente monitoreadas por el sector directivo del hotel.</li>
            </ul>
            <li>Notificación del caso. Si un huésped comienza a tener síntomas, será alentado a contactar a las autoridades de salud. Se implementarán los procedimientos de cuarentena y protocolo de limpieza adecuado.</li>
            <li>Protocolo de Recuperación de habitación Ocupada por el huésped. En caso de que a un huésped se le confirme o se sospeche un caso de Covid-19, la habitación del huésped será retirada de servicio y se someterá al protocolo específico de limpieza. La habitación del huésped no regresará al servicio hasta que sea desinfectada.
            Se aplicarán protocolos de recuperación similares, a las oficinas de empleados y áreas de trabajo.</li>
          </ul>
        </p>

